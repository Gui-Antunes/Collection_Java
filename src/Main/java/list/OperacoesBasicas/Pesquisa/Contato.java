package Main.java.list.OperacoesBasicas.Pesquisa;

public class Contato {
    //atributo
    private String nome;
    private int numero;

    public Contato(String nome, int numero) {
        this.nome = nome;
        this.numero = numero;
    }

    public String getNome() {
        return nome;
    }

    public int getNumero() {
        return numero;
    }

}
